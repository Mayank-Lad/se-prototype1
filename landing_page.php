                New Delhi: Latest payroll data released by the Employees' Provident Fund Organisation (EPFO) on Thursday showed that formal job creation has slowed in the month of May. It had earlier recovered a little as the new fiscal year began, Business Standard reported.

April had seen 891,974 new EPF subscribers, and this fell by about 1% in May to 883,176. However, the proportion of younger new subscribers (18-28 years) has grown, from 64.6% of the new subscribers in April to 66.8% in May.

The share of women subscribers has fallen slightly, from 26.5% in April to 25.03% in May.

Explaining the slowdown, Lohit Bhatia, president, Indian Staffing Federation, told Business Standard, “The month of May coincides with the onset of monsoons and there is a reverse migration to rural areas, thus leaving a lot of people out of the social security organisation's purview. Moreover, the festival season is some time away, and firms are usually lacklustre in hiring people at this time.”

Meanwhile, the Centre for Monitoring Indian Economy has found that the unemployment rate had fallen to 7.7% in May from 8.11% in April  but because there was a decline in the labour force participation rate (LPR) to 39.6% from 40.7%, and not because of job creation.

“This drop in the LPR in May was expected because April witnessed a large number of people entering the labour force but only a small share was able to secure employment in the month. This was bound to discourage many from searching for work in May. As a result, the labour force shrank in size from 453.5 million to 441.9 million,” a CMIE statement said.
